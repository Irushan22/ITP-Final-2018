    private void teletxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_teletxtKeyPressed
        int key3=evt.getKeyCode();
        
        if((key3>=java.awt.event.KeyEvent.VK_0&&key3<=java.awt.event.KeyEvent.VK_9)||
                (key3>=java.awt.event.KeyEvent.VK_NUMPAD0&&key3<=java.awt.event.KeyEvent.VK_NUMPAD9)||
                key3==KeyEvent.VK_BACKSPACE)
        {
            teletxt.setEditable(true);
            teletxt.setBackground(Color.WHITE);
            jLabel1.setText("");
        }
        else
        {
            teletxt.setEditable(false);
            //quentitytxt.setBackground(Color.RED);
            jLabel1.setText("* invalid Telephone");
        }
    }//GEN-LAST:event_teletxtKeyPressed

