    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
       String id = ITMcode.getText();
       String name = ITMname.getText();
       String cat = category.getSelectedItem().toString();
       String qunty = qty.getText();
       String stk = stock.getText();
       String uPrc = UnitPrc.getText();
       String sPrc = SalePrc.getText();
       
       if(id.equals("") | name.equals("") | qunty.equals("") | stk.equals("") | uPrc.equals("") | sPrc.equals("")){
           JOptionPane.showMessageDialog(rootPane, "Please Fill The Fields");
       }
       else{
           String query = "INSERT INTO stock(ItemCode, ItemName, Category, Qty,Stock, UnitPrice, SalePrice) VALUES('"+ id +"', '"+ name +"', '"+ cat +"', '"+ qunty +"', '"+ stk +"', '"+ uPrc +"', '"+ sPrc +"')";
           
           try{
                  
                Connection con = connect();
                Statement st = con.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(rootPane, "Record has been saved successfully");
                clearAll();
                tableLoad();
           
            }catch(SQLException e){
                System.out.println(e);
            }catch(ClassNotFoundException ex){
                Logger.getLogger(InventoryManagement.class.getName()).log(Level.SEVERE,null,ex);
            }
       }
 
    }//GEN-LAST:event_addActionPerformed

